# how to print single star
print('*')
# output :- *

# how to print multiple star in single row
a = ('* ')
for i in range(4):
    print(a ,end = "")
# output :- * * * *

# how to print multiple star in multiple row
a = ('*')
for i in range(4):
    print(a)

#output :-      *
#               *
#               *
#               *
